# function - performs a particular task
# module - a collection of several similar functions in a single file
# package - collection of various modules organized in a directory hierarchy.

def sum_a_b(a,b) :
    return a+b

def diff_a_b(a,b) :
    return a-b

def prd_a_b(a,b) :
    return a*b

def mod_a_b(a,b) :
    return a%b